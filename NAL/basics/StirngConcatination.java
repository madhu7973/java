package basics;

public class StirngConcatination {
	
	public static void main(String[] args) {
		
		int i = 10;
		int j = 20;
		
		String s = "Hello";
		String n = "World";
		
		System.out.println(i+j);
		
		System.out.println(s+" "+n);
		
		System.out.println(s+n+i+j);
		
		System.out.println(i+s+j+n);
		
		System.out.println(i+j+s+n);
		
		System.out.println(s + n + (i + j));
	}

}
