public class FinalMethodSup 
{
	final void m1()
	{
		System.out.println("This is a finall method");
	}
	final void m1(int x)
	{
		System.out.println("This is overloaded single parameterized final method");
	}
	final void m1 (int x, int y)
	{
		System.out.println("This is overloaded dual parameterized final method");
	}
}
